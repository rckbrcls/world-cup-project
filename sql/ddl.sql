CREATE OR REPLACE FUNCTION fn_validate_call_up()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    IF EXISTS (
        SELECT 1
        FROM team_call_up existing_call_up
        WHERE existing_call_up.edition_id = NEW.edition_id
          AND existing_call_up.player_id = NEW.player_id
          AND existing_call_up.team_id <> NEW.team_id
    ) THEN
        RAISE EXCEPTION 'Player % is already assigned to another team in edition %.', NEW.player_id, NEW.edition_id;
    END IF;

    IF EXISTS (
        SELECT 1
        FROM team_call_up existing_call_up
        WHERE existing_call_up.player_id = NEW.player_id
          AND existing_call_up.team_id <> NEW.team_id
    ) THEN
        RAISE EXCEPTION 'Player % cannot switch national teams across editions.', NEW.player_id;
    END IF;

    IF NEW.is_captain AND EXISTS (
        SELECT 1
        FROM team_call_up existing_call_up
        WHERE existing_call_up.edition_id = NEW.edition_id
          AND existing_call_up.team_id = NEW.team_id
          AND existing_call_up.is_captain = TRUE
          AND existing_call_up.player_id <> NEW.player_id
    ) THEN
        RAISE EXCEPTION 'Team % in edition % already has a captain.', NEW.team_id, NEW.edition_id;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_validate_edition_podium(p_edition_id BIGINT)
RETURNS VOID
LANGUAGE plpgsql
AS $$
DECLARE
    current_edition world_cup_edition%ROWTYPE;
    rank_one_team_id BIGINT;
    rank_two_team_id BIGINT;
    rank_three_team_id BIGINT;
    rank_one_count BIGINT;
    rank_two_count BIGINT;
    rank_three_count BIGINT;
BEGIN
    SELECT *
    INTO current_edition
    FROM world_cup_edition edition_row
    WHERE edition_row.id = p_edition_id;

    IF NOT FOUND THEN
        RETURN;
    END IF;

    IF current_edition.champion_team_id IS NOT NULL AND NOT EXISTS (
        SELECT 1
        FROM edition_team edition_team_row
        WHERE edition_team_row.edition_id = current_edition.id
          AND edition_team_row.team_id = current_edition.champion_team_id
    ) THEN
        RAISE EXCEPTION 'Champion team % must belong to edition %.', current_edition.champion_team_id, current_edition.id;
    END IF;

    IF current_edition.vice_champion_team_id IS NOT NULL AND NOT EXISTS (
        SELECT 1
        FROM edition_team edition_team_row
        WHERE edition_team_row.edition_id = current_edition.id
          AND edition_team_row.team_id = current_edition.vice_champion_team_id
    ) THEN
        RAISE EXCEPTION 'Vice champion team % must belong to edition %.', current_edition.vice_champion_team_id, current_edition.id;
    END IF;

    IF current_edition.third_place_team_id IS NOT NULL AND NOT EXISTS (
        SELECT 1
        FROM edition_team edition_team_row
        WHERE edition_team_row.edition_id = current_edition.id
          AND edition_team_row.team_id = current_edition.third_place_team_id
    ) THEN
        RAISE EXCEPTION 'Third-place team % must belong to edition %.', current_edition.third_place_team_id, current_edition.id;
    END IF;

    SELECT COUNT(*)::BIGINT
    INTO rank_one_count
    FROM edition_team edition_team_row
    WHERE edition_team_row.edition_id = current_edition.id
      AND edition_team_row.final_rank = 1;

    SELECT COUNT(*)::BIGINT
    INTO rank_two_count
    FROM edition_team edition_team_row
    WHERE edition_team_row.edition_id = current_edition.id
      AND edition_team_row.final_rank = 2;

    SELECT COUNT(*)::BIGINT
    INTO rank_three_count
    FROM edition_team edition_team_row
    WHERE edition_team_row.edition_id = current_edition.id
      AND edition_team_row.final_rank = 3;

    IF rank_one_count > 1 THEN
        RAISE EXCEPTION 'Edition % cannot have more than one team ranked 1.', current_edition.id;
    END IF;

    IF rank_two_count > 1 THEN
        RAISE EXCEPTION 'Edition % cannot have more than one team ranked 2.', current_edition.id;
    END IF;

    IF rank_three_count > 1 THEN
        RAISE EXCEPTION 'Edition % cannot have more than one team ranked 3.', current_edition.id;
    END IF;

    SELECT edition_team_row.team_id
    INTO rank_one_team_id
    FROM edition_team edition_team_row
    WHERE edition_team_row.edition_id = current_edition.id
      AND edition_team_row.final_rank = 1;

    SELECT edition_team_row.team_id
    INTO rank_two_team_id
    FROM edition_team edition_team_row
    WHERE edition_team_row.edition_id = current_edition.id
      AND edition_team_row.final_rank = 2;

    SELECT edition_team_row.team_id
    INTO rank_three_team_id
    FROM edition_team edition_team_row
    WHERE edition_team_row.edition_id = current_edition.id
      AND edition_team_row.final_rank = 3;

    IF current_edition.champion_team_id IS NOT NULL AND rank_one_team_id IS NULL THEN
        RAISE EXCEPTION 'Champion team for edition % requires one edition team ranked 1.', current_edition.id;
    END IF;

    IF current_edition.vice_champion_team_id IS NOT NULL AND rank_two_team_id IS NULL THEN
        RAISE EXCEPTION 'Vice champion team for edition % requires one edition team ranked 2.', current_edition.id;
    END IF;

    IF current_edition.third_place_team_id IS NOT NULL AND rank_three_team_id IS NULL THEN
        RAISE EXCEPTION 'Third-place team for edition % requires one edition team ranked 3.', current_edition.id;
    END IF;

    IF rank_one_team_id IS NOT NULL
       AND current_edition.champion_team_id IS DISTINCT FROM rank_one_team_id THEN
        RAISE EXCEPTION 'Champion team for edition % must match the team ranked 1.', current_edition.id;
    END IF;

    IF rank_two_team_id IS NOT NULL
       AND current_edition.vice_champion_team_id IS DISTINCT FROM rank_two_team_id THEN
        RAISE EXCEPTION 'Vice champion team for edition % must match the team ranked 2.', current_edition.id;
    END IF;

    IF rank_three_team_id IS NOT NULL
       AND current_edition.third_place_team_id IS DISTINCT FROM rank_three_team_id THEN
        RAISE EXCEPTION 'Third-place team for edition % must match the team ranked 3.', current_edition.id;
    END IF;
END;
$$;

CREATE OR REPLACE FUNCTION fn_validate_edition_structure(p_edition_id BIGINT)
RETURNS VOID
LANGUAGE plpgsql
AS $$
DECLARE
    current_host_country_id BIGINT;
BEGIN
    SELECT edition_row.host_country_id
    INTO current_host_country_id
    FROM world_cup_edition edition_row
    WHERE edition_row.id = p_edition_id;

    IF current_host_country_id IS NULL THEN
        RETURN;
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM edition_host_city edition_host_city_row
        WHERE edition_host_city_row.edition_id = p_edition_id
    ) THEN
        RAISE EXCEPTION 'Edition % must have at least one host city.', p_edition_id;
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM edition_phase edition_phase_row
        WHERE edition_phase_row.edition_id = p_edition_id
    ) THEN
        RAISE EXCEPTION 'Edition % must have at least one phase.', p_edition_id;
    END IF;

    IF EXISTS (
        SELECT 1
        FROM edition_host_city edition_host_city_row
        JOIN host_city host_city_row ON host_city_row.id = edition_host_city_row.host_city_id
        WHERE edition_host_city_row.edition_id = p_edition_id
          AND host_city_row.country_id <> current_host_country_id
    ) THEN
        RAISE EXCEPTION 'Every host city for edition % must belong to the edition host country.', p_edition_id;
    END IF;
END;
$$;

CREATE OR REPLACE FUNCTION fn_enforce_edition_structure()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    target_edition_id BIGINT;
BEGIN
    IF TG_TABLE_NAME = 'world_cup_edition' THEN
        IF TG_OP = 'DELETE' THEN
            target_edition_id := OLD.id;
        ELSE
            target_edition_id := NEW.id;
        END IF;
    ELSE
        IF TG_OP = 'DELETE' THEN
            target_edition_id := OLD.edition_id;
        ELSE
            target_edition_id := NEW.edition_id;
        END IF;
    END IF;

    PERFORM fn_validate_edition_structure(target_edition_id);

    RETURN NULL;
END;
$$;

