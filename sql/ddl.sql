BEGIN;

CREATE SCHEMA IF NOT EXISTS world_cup;
SET search_path TO world_cup, public;

CREATE TYPE phase_kind AS ENUM ('GROUP', 'KNOCKOUT', 'PLACEMENT');
CREATE TYPE official_role AS ENUM ('MAIN_REFEREE', 'ASSISTANT_REFEREE_1', 'ASSISTANT_REFEREE_2', 'FOURTH_OFFICIAL');
CREATE TYPE game_event_type AS ENUM (
    'GOAL',
    'OWN_GOAL',
    'PENALTY_GOAL',
    'YELLOW_CARD',
    'RED_CARD',
    'SUBSTITUTION'
);

CREATE TABLE confederation (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(10) NOT NULL UNIQUE,
    name VARCHAR(100) NOT NULL UNIQUE,
    CONSTRAINT confederation_code_uppercase CHECK (code = UPPER(code))
);

CREATE TABLE country (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    fifa_code CHAR(3) NOT NULL UNIQUE,
    confederation_id BIGINT NOT NULL REFERENCES confederation (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT country_fifa_code_uppercase CHECK (fifa_code = UPPER(fifa_code))
);

CREATE TABLE team (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(100) NOT NULL UNIQUE,
    fifa_code CHAR(3) NOT NULL UNIQUE,
    country_id BIGINT NOT NULL UNIQUE REFERENCES country (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT team_fifa_code_uppercase CHECK (fifa_code = UPPER(fifa_code))
);

CREATE TABLE coach (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name VARCHAR(120) NOT NULL,
    birth_date DATE,
    country_id BIGINT REFERENCES country (id) ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE referee (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name VARCHAR(120) NOT NULL,
    country_id BIGINT REFERENCES country (id) ON UPDATE CASCADE ON DELETE SET NULL,
    UNIQUE (full_name, country_id)
);

CREATE TABLE player (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    full_name VARCHAR(120) NOT NULL,
    birth_date DATE NOT NULL,
    primary_position VARCHAR(40) NOT NULL,
    country_of_birth_id BIGINT REFERENCES country (id) ON UPDATE CASCADE ON DELETE SET NULL,
    UNIQUE (full_name, birth_date)
);

CREATE TABLE world_cup_edition (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    year SMALLINT NOT NULL UNIQUE,
    host_country_id BIGINT NOT NULL REFERENCES country (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    champion_team_id BIGINT REFERENCES team (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    vice_champion_team_id BIGINT REFERENCES team (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    third_place_team_id BIGINT REFERENCES team (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    CONSTRAINT edition_year_range CHECK (year BETWEEN 1930 AND 2100),
    CONSTRAINT edition_date_range CHECK (start_date <= end_date),
    CONSTRAINT edition_distinct_top_two CHECK (
        champion_team_id IS NULL
        OR vice_champion_team_id IS NULL
        OR champion_team_id <> vice_champion_team_id
    ),
    CONSTRAINT edition_distinct_third_place CHECK (
        third_place_team_id IS NULL
        OR (
            champion_team_id IS NULL
            OR third_place_team_id <> champion_team_id
        ) AND (
            vice_champion_team_id IS NULL
            OR third_place_team_id <> vice_champion_team_id
        )
    )
);

CREATE TABLE host_city (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(120) NOT NULL,
    country_id BIGINT NOT NULL REFERENCES country (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    UNIQUE (name, country_id)
);

CREATE TABLE stadium (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(120) NOT NULL,
    host_city_id BIGINT NOT NULL REFERENCES host_city (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    capacity INTEGER NOT NULL,
    CONSTRAINT stadium_capacity_positive CHECK (capacity > 0),
    UNIQUE (name, host_city_id)
);

CREATE TABLE edition_host_city (
    edition_id BIGINT NOT NULL REFERENCES world_cup_edition (id) ON UPDATE CASCADE ON DELETE CASCADE,
    host_city_id BIGINT NOT NULL REFERENCES host_city (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    PRIMARY KEY (edition_id, host_city_id)
);

CREATE TABLE competition_phase (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code VARCHAR(30) NOT NULL UNIQUE,
    display_name VARCHAR(120) NOT NULL UNIQUE,
    phase_kind phase_kind NOT NULL,
    sort_order SMALLINT NOT NULL UNIQUE,
    allows_draw BOOLEAN NOT NULL DEFAULT FALSE,
    CONSTRAINT competition_phase_code_uppercase CHECK (code = UPPER(code))
);

CREATE TABLE edition_phase (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    edition_id BIGINT NOT NULL REFERENCES world_cup_edition (id) ON UPDATE CASCADE ON DELETE CASCADE,
    phase_id BIGINT NOT NULL REFERENCES competition_phase (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    label VARCHAR(120) NOT NULL,
    stage_order SMALLINT NOT NULL,
    UNIQUE (edition_id, phase_id),
    UNIQUE (id, edition_id),
    CONSTRAINT edition_phase_order_positive CHECK (stage_order > 0)
);

CREATE TABLE team_group (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    edition_id BIGINT NOT NULL REFERENCES world_cup_edition (id) ON UPDATE CASCADE ON DELETE CASCADE,
    group_letter CHAR(1) NOT NULL,
    UNIQUE (edition_id, group_letter),
    UNIQUE (id, edition_id),
    CONSTRAINT team_group_letter_uppercase CHECK (group_letter = UPPER(group_letter)),
    CONSTRAINT team_group_letter_alpha CHECK (group_letter ~ '^[A-Z]$')
);

CREATE TABLE edition_team (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    edition_id BIGINT NOT NULL REFERENCES world_cup_edition (id) ON UPDATE CASCADE ON DELETE CASCADE,
    team_id BIGINT NOT NULL REFERENCES team (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    coach_id BIGINT NOT NULL REFERENCES coach (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    group_id BIGINT,
    final_rank SMALLINT,
    UNIQUE (edition_id, team_id),
    UNIQUE (edition_id, coach_id),
    UNIQUE (id, edition_id),
    CONSTRAINT edition_team_rank_range CHECK (final_rank IS NULL OR final_rank BETWEEN 1 AND 64),
    CONSTRAINT edition_team_group_fk FOREIGN KEY (group_id, edition_id)
        REFERENCES team_group (id, edition_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL
);

CREATE TABLE team_call_up (
    edition_id BIGINT NOT NULL,
    team_id BIGINT NOT NULL,
    player_id BIGINT NOT NULL REFERENCES player (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    shirt_number SMALLINT NOT NULL,
    squad_role VARCHAR(30) NOT NULL DEFAULT 'PLAYER',
    primary_position VARCHAR(40) NOT NULL,
    is_captain BOOLEAN NOT NULL DEFAULT FALSE,
    PRIMARY KEY (edition_id, team_id, player_id),
    UNIQUE (edition_id, player_id),
    UNIQUE (edition_id, team_id, shirt_number),
    CONSTRAINT team_call_up_shirt_number_range CHECK (shirt_number BETWEEN 1 AND 99),
    CONSTRAINT team_call_up_role_uppercase CHECK (squad_role = UPPER(squad_role)),
    CONSTRAINT team_call_up_team_fk FOREIGN KEY (edition_id, team_id)
        REFERENCES edition_team (edition_id, team_id)
        ON UPDATE CASCADE
        ON DELETE CASCADE
);

CREATE TABLE match_game (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    edition_id BIGINT NOT NULL REFERENCES world_cup_edition (id) ON UPDATE CASCADE ON DELETE CASCADE,
    edition_phase_id BIGINT NOT NULL,
    group_id BIGINT,
    stadium_id BIGINT NOT NULL REFERENCES stadium (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    kickoff_at TIMESTAMP NOT NULL,
    home_team_id BIGINT NOT NULL,
    away_team_id BIGINT NOT NULL,
    winner_team_id BIGINT,
    home_score SMALLINT NOT NULL DEFAULT 0,
    away_score SMALLINT NOT NULL DEFAULT 0,
    home_extra_score SMALLINT NOT NULL DEFAULT 0,
    away_extra_score SMALLINT NOT NULL DEFAULT 0,
    home_penalty_score SMALLINT,
    away_penalty_score SMALLINT,
    match_day SMALLINT,
    CONSTRAINT match_phase_fk FOREIGN KEY (edition_phase_id, edition_id)
        REFERENCES edition_phase (id, edition_id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT match_group_fk FOREIGN KEY (group_id, edition_id)
        REFERENCES team_group (id, edition_id)
        ON UPDATE CASCADE
        ON DELETE SET NULL,
    CONSTRAINT match_home_team_fk FOREIGN KEY (edition_id, home_team_id)
        REFERENCES edition_team (edition_id, team_id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT match_away_team_fk FOREIGN KEY (edition_id, away_team_id)
        REFERENCES edition_team (edition_id, team_id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT match_winner_team_fk FOREIGN KEY (edition_id, winner_team_id)
        REFERENCES edition_team (edition_id, team_id)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    CONSTRAINT match_distinct_teams CHECK (home_team_id <> away_team_id),
    CONSTRAINT match_score_non_negative CHECK (
        home_score >= 0
        AND away_score >= 0
        AND home_extra_score >= 0
        AND away_extra_score >= 0
        AND (home_penalty_score IS NULL OR home_penalty_score >= 0)
        AND (away_penalty_score IS NULL OR away_penalty_score >= 0)
    ),
    CONSTRAINT match_penalties_null_together CHECK (
        (home_penalty_score IS NULL AND away_penalty_score IS NULL)
        OR (home_penalty_score IS NOT NULL AND away_penalty_score IS NOT NULL)
    ),
    CONSTRAINT match_winner_is_participant CHECK (
        winner_team_id IS NULL OR winner_team_id IN (home_team_id, away_team_id)
    )
);

CREATE TABLE match_official (
    match_id BIGINT NOT NULL REFERENCES match_game (id) ON UPDATE CASCADE ON DELETE CASCADE,
    referee_id BIGINT NOT NULL REFERENCES referee (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    role official_role NOT NULL,
    PRIMARY KEY (match_id, referee_id),
    UNIQUE (match_id, role)
);

CREATE TABLE match_event (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    match_id BIGINT NOT NULL REFERENCES match_game (id) ON UPDATE CASCADE ON DELETE CASCADE,
    event_minute SMALLINT NOT NULL,
    stoppage_minute SMALLINT NOT NULL DEFAULT 0,
    event_type game_event_type NOT NULL,
    team_id BIGINT NOT NULL REFERENCES team (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    player_id BIGINT REFERENCES player (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    related_player_id BIGINT REFERENCES player (id) ON UPDATE CASCADE ON DELETE RESTRICT,
    description TEXT,
    CONSTRAINT match_event_minute_range CHECK (event_minute BETWEEN 0 AND 130),
    CONSTRAINT match_event_stoppage_range CHECK (stoppage_minute BETWEEN 0 AND 30),
    CONSTRAINT match_event_distinct_players CHECK (
        related_player_id IS NULL OR player_id IS NULL OR related_player_id <> player_id
    )
);

