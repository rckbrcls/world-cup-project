CREATE OR REPLACE FUNCTION fn_validate_match_game()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    current_phase_kind phase_kind;
    current_allows_draw BOOLEAN;
    home_total_goals INTEGER;
    away_total_goals INTEGER;
BEGIN
    SELECT phase.phase_kind, phase.allows_draw
    INTO current_phase_kind, current_allows_draw
    FROM edition_phase edition_phase_row
    JOIN competition_phase phase ON phase.id = edition_phase_row.phase_id
    WHERE edition_phase_row.id = NEW.edition_phase_id
      AND edition_phase_row.edition_id = NEW.edition_id;

    IF current_phase_kind IS NULL THEN
        RAISE EXCEPTION 'Edition phase % does not belong to edition %.', NEW.edition_phase_id, NEW.edition_id;
    END IF;

    IF NOT EXISTS (
        SELECT 1
        FROM edition_host_city edition_host_city_row
        JOIN stadium stadium_row ON stadium_row.host_city_id = edition_host_city_row.host_city_id
        WHERE edition_host_city_row.edition_id = NEW.edition_id
          AND stadium_row.id = NEW.stadium_id
    ) THEN
        RAISE EXCEPTION 'Stadium % is not registered as a host venue for edition %.', NEW.stadium_id, NEW.edition_id;
    END IF;

    home_total_goals := NEW.home_score + NEW.home_extra_score;
    away_total_goals := NEW.away_score + NEW.away_extra_score;

    IF current_phase_kind = 'GROUP' THEN
        IF NEW.group_id IS NULL THEN
            RAISE EXCEPTION 'Group-stage matches must belong to a group.';
        END IF;

        IF EXISTS (
            SELECT 1
            FROM edition_team edition_team_row
            WHERE edition_team_row.edition_id = NEW.edition_id
              AND edition_team_row.team_id IN (NEW.home_team_id, NEW.away_team_id)
              AND edition_team_row.group_id IS DISTINCT FROM NEW.group_id
        ) THEN
            RAISE EXCEPTION 'Both teams must belong to the same group for a group-stage match.';
        END IF;

        IF NEW.home_penalty_score IS NOT NULL OR NEW.away_penalty_score IS NOT NULL THEN
            RAISE EXCEPTION 'Group-stage matches cannot have penalty shootouts.';
        END IF;

        IF NEW.winner_team_id IS NOT NULL THEN
            IF home_total_goals = away_total_goals THEN
                RAISE EXCEPTION 'Drawn group-stage matches cannot declare a winner.';
            END IF;

            IF (
                home_total_goals > away_total_goals AND NEW.winner_team_id <> NEW.home_team_id
            ) OR (
                away_total_goals > home_total_goals AND NEW.winner_team_id <> NEW.away_team_id
            ) THEN
                RAISE EXCEPTION 'Winner team does not match the scoreline for group-stage match %.', NEW.id;
            END IF;
        END IF;
    ELSE
        IF NEW.group_id IS NOT NULL THEN
            RAISE EXCEPTION 'Knockout and placement matches cannot reference a group.';
        END IF;

        IF NEW.winner_team_id IS NULL THEN
            RAISE EXCEPTION 'Non-group matches must declare a qualified team.';
        END IF;

        IF current_allows_draw THEN
            RAISE EXCEPTION 'Knockout-style phases cannot allow draws.';
        END IF;

        IF home_total_goals = away_total_goals THEN
            IF NEW.home_penalty_score IS NULL OR NEW.away_penalty_score IS NULL THEN
                RAISE EXCEPTION 'Tied knockout matches require penalty scores.';
            END IF;

            IF NEW.home_penalty_score = NEW.away_penalty_score THEN
                RAISE EXCEPTION 'Penalty shootouts cannot finish tied.';
            END IF;

            IF (
                NEW.home_penalty_score > NEW.away_penalty_score AND NEW.winner_team_id <> NEW.home_team_id
            ) OR (
                NEW.away_penalty_score > NEW.home_penalty_score AND NEW.winner_team_id <> NEW.away_team_id
            ) THEN
                RAISE EXCEPTION 'Winner team does not match the penalty shootout outcome.';
            END IF;
        ELSE
            IF NEW.home_penalty_score IS NOT NULL OR NEW.away_penalty_score IS NOT NULL THEN
                RAISE EXCEPTION 'Penalty scores are only allowed when the knockout match is tied after extra time.';
            END IF;

            IF (
                home_total_goals > away_total_goals AND NEW.winner_team_id <> NEW.home_team_id
            ) OR (
                away_total_goals > home_total_goals AND NEW.winner_team_id <> NEW.away_team_id
            ) THEN
                RAISE EXCEPTION 'Winner team does not match the final score.';
            END IF;
        END IF;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_validate_match_event()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    current_edition_id BIGINT;
    current_home_team_id BIGINT;
    current_away_team_id BIGINT;
BEGIN
    SELECT match_row.edition_id, match_row.home_team_id, match_row.away_team_id
    INTO current_edition_id, current_home_team_id, current_away_team_id
    FROM match_game match_row
    WHERE match_row.id = NEW.match_id;

    IF current_edition_id IS NULL THEN
        RAISE EXCEPTION 'Match % does not exist.', NEW.match_id;
    END IF;

    IF NEW.team_id NOT IN (current_home_team_id, current_away_team_id) THEN
        RAISE EXCEPTION 'Event team % is not part of match %.', NEW.team_id, NEW.match_id;
    END IF;

    IF NEW.event_type = 'SUBSTITUTION' THEN
        IF NEW.player_id IS NULL OR NEW.related_player_id IS NULL THEN
            RAISE EXCEPTION 'Substitution events must declare outgoing and incoming players.';
        END IF;
    ELSE
        IF NEW.player_id IS NULL THEN
            RAISE EXCEPTION 'Event type % requires a player.', NEW.event_type;
        END IF;

        IF NEW.related_player_id IS NOT NULL THEN
            RAISE EXCEPTION 'Event type % cannot declare a secondary player.', NEW.event_type;
        END IF;
    END IF;

    IF NEW.player_id IS NOT NULL AND NOT EXISTS (
        SELECT 1
        FROM team_call_up call_up
        WHERE call_up.edition_id = current_edition_id
          AND call_up.team_id = NEW.team_id
          AND call_up.player_id = NEW.player_id
    ) THEN
        RAISE EXCEPTION 'Player % is not registered for team % in edition %.', NEW.player_id, NEW.team_id, current_edition_id;
    END IF;

    IF NEW.related_player_id IS NOT NULL AND NOT EXISTS (
        SELECT 1
        FROM team_call_up call_up
        WHERE call_up.edition_id = current_edition_id
          AND call_up.team_id = NEW.team_id
          AND call_up.player_id = NEW.related_player_id
    ) THEN
        RAISE EXCEPTION 'Related player % is not registered for team % in edition %.', NEW.related_player_id, NEW.team_id, current_edition_id;
    END IF;

    RETURN NEW;
END;
$$;

CREATE OR REPLACE FUNCTION fn_validate_generated_sql(p_sql TEXT)
RETURNS TEXT
LANGUAGE plpgsql
AS $$
DECLARE
    normalized_sql TEXT;
BEGIN
    normalized_sql := btrim(p_sql);

    IF normalized_sql IS NULL OR normalized_sql = '' THEN
        RAISE EXCEPTION 'Generated SQL cannot be empty.';
    END IF;

    normalized_sql := regexp_replace(normalized_sql, ';+\s*$', '');
    normalized_sql := btrim(normalized_sql);

    IF normalized_sql = '' THEN
        RAISE EXCEPTION 'Generated SQL cannot be empty after normalization.';
    END IF;

    IF normalized_sql ~ ';' THEN
        RAISE EXCEPTION 'Generated SQL must contain exactly one statement.';
    END IF;

    IF normalized_sql ~ '(^|[^:])--' OR normalized_sql ~ '/\*' THEN
        RAISE EXCEPTION 'Generated SQL cannot contain SQL comments.';
    END IF;

    IF normalized_sql !~* '^(SELECT|WITH)\M' THEN
        RAISE EXCEPTION 'Generated SQL must start with SELECT or WITH.';
    END IF;

    IF normalized_sql ~* '\m(INSERT|UPDATE|DELETE|MERGE|DROP|ALTER|CREATE|TRUNCATE|GRANT|REVOKE|COPY|CALL|DO|EXECUTE|PREPARE|DEALLOCATE|VACUUM|ANALYZE|REFRESH|SET|RESET|SHOW|BEGIN|COMMIT|ROLLBACK|SAVEPOINT|LOCK|DISCARD|LISTEN|NOTIFY|UNLISTEN)\M' THEN
        RAISE EXCEPTION 'Generated SQL contains a forbidden command.';
    END IF;

    IF normalized_sql ~* '\m(pg_catalog|information_schema|pg_toast|pg_temp)\.' THEN
        RAISE EXCEPTION 'Generated SQL cannot access PostgreSQL system schemas.';
    END IF;

    RETURN normalized_sql;
END;
$$;

