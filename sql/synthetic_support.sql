            away_team_row.team_id AS away_team_id,
            CASE
                WHEN group_fixture_template.home_score > group_fixture_template.away_score THEN home_team_row.team_id
                WHEN group_fixture_template.away_score > group_fixture_template.home_score THEN away_team_row.team_id
                ELSE NULL
            END AS winner_team_id,
            group_fixture_template.home_score,
            group_fixture_template.away_score,
            0::SMALLINT AS home_extra_score,
            0::SMALLINT AS away_extra_score,
            NULL::SMALLINT AS home_penalty_score,
            NULL::SMALLINT AS away_penalty_score,
            group_fixture_template.match_day
        FROM group_fixture_template
        JOIN group_index_seed ON TRUE
        JOIN world_cup_edition edition_row ON edition_row.year IN (2034, 2038)
        JOIN edition_phase edition_phase_row
            ON edition_phase_row.edition_id = edition_row.id
        JOIN competition_phase phase_row
            ON phase_row.id = edition_phase_row.phase_id
           AND phase_row.code = 'GROUP_STAGE'
        JOIN team_group team_group_row
            ON team_group_row.edition_id = edition_row.id
           AND team_group_row.group_letter = group_index_seed.group_letter
        JOIN edition_team_seed home_seed
            ON home_seed.edition_year = edition_row.year
           AND home_seed.group_letter = group_index_seed.group_letter
           AND home_seed.group_slot = group_fixture_template.home_slot
        JOIN edition_team_seed away_seed
            ON away_seed.edition_year = edition_row.year
           AND away_seed.group_letter = group_index_seed.group_letter
           AND away_seed.group_slot = group_fixture_template.away_slot
        JOIN edition_team home_team_row
            ON home_team_row.edition_id = edition_row.id
        JOIN team home_team_catalog ON home_team_catalog.id = home_team_row.team_id
           AND home_team_catalog.fifa_code = home_seed.team_code
        JOIN edition_team away_team_row
            ON away_team_row.edition_id = edition_row.id
        JOIN team away_team_catalog ON away_team_catalog.id = away_team_row.team_id
           AND away_team_catalog.fifa_code = away_seed.team_code
        JOIN venue_slot_seed
            ON venue_slot_seed.edition_year = edition_row.year
           AND venue_slot_seed.venue_slot = group_index_seed.group_index
        JOIN stadium stadium_row ON stadium_row.name = venue_slot_seed.stadium_name
    ),
    knockout_seed AS (
        SELECT *
        FROM jsonb_to_recordset(v_knockout_catalog) AS knockout_seed(
            edition_year SMALLINT,
            phase_code TEXT,
            kickoff_at TIMESTAMP,
            stadium_name TEXT,
            home_slot_ref TEXT,
            away_slot_ref TEXT,
            winner_slot_ref TEXT,
            home_score SMALLINT,
            away_score SMALLINT,
            home_extra_score SMALLINT,
            away_extra_score SMALLINT,
            home_penalty_score SMALLINT,
            away_penalty_score SMALLINT
        )
    ),
    knockout_match_seed AS (
        SELECT
            edition_row.id AS edition_id,
            edition_phase_row.id AS edition_phase_id,
            NULL::BIGINT AS group_id,
            stadium_row.id AS stadium_id,
            knockout_seed.kickoff_at,
            home_team_row.team_id AS home_team_id,
            away_team_row.team_id AS away_team_id,
            winner_team_row.team_id AS winner_team_id,
            knockout_seed.home_score,
            knockout_seed.away_score,
            knockout_seed.home_extra_score,
            knockout_seed.away_extra_score,
            knockout_seed.home_penalty_score,
            knockout_seed.away_penalty_score,
            NULL::SMALLINT AS match_day
        FROM knockout_seed
        JOIN world_cup_edition edition_row ON edition_row.year = knockout_seed.edition_year
        JOIN edition_phase edition_phase_row
            ON edition_phase_row.edition_id = edition_row.id
        JOIN competition_phase phase_row
            ON phase_row.id = edition_phase_row.phase_id
           AND phase_row.code = knockout_seed.phase_code
        JOIN edition_team_seed home_seed
            ON home_seed.edition_year = knockout_seed.edition_year
           AND home_seed.group_letter = LEFT(knockout_seed.home_slot_ref, 1)
           AND home_seed.group_slot = RIGHT(knockout_seed.home_slot_ref, 1)::SMALLINT
        JOIN edition_team_seed away_seed
            ON away_seed.edition_year = knockout_seed.edition_year
           AND away_seed.group_letter = LEFT(knockout_seed.away_slot_ref, 1)
           AND away_seed.group_slot = RIGHT(knockout_seed.away_slot_ref, 1)::SMALLINT
        JOIN edition_team_seed winner_seed
            ON winner_seed.edition_year = knockout_seed.edition_year
           AND winner_seed.group_letter = LEFT(knockout_seed.winner_slot_ref, 1)
           AND winner_seed.group_slot = RIGHT(knockout_seed.winner_slot_ref, 1)::SMALLINT
        JOIN edition_team home_team_row
            ON home_team_row.edition_id = edition_row.id
        JOIN team home_team_catalog ON home_team_catalog.id = home_team_row.team_id
           AND home_team_catalog.fifa_code = home_seed.team_code
        JOIN edition_team away_team_row
            ON away_team_row.edition_id = edition_row.id
        JOIN team away_team_catalog ON away_team_catalog.id = away_team_row.team_id
           AND away_team_catalog.fifa_code = away_seed.team_code
        JOIN edition_team winner_team_row
            ON winner_team_row.edition_id = edition_row.id
        JOIN team winner_team_catalog ON winner_team_catalog.id = winner_team_row.team_id
           AND winner_team_catalog.fifa_code = winner_seed.team_code
        JOIN stadium stadium_row ON stadium_row.name = knockout_seed.stadium_name
    ),
    inserted AS (
        INSERT INTO match_game (
            edition_id,
            edition_phase_id,
            group_id,
            stadium_id,
            kickoff_at,
            home_team_id,
            away_team_id,
            winner_team_id,
            home_score,
            away_score,
            home_extra_score,
            away_extra_score,
            home_penalty_score,
            away_penalty_score,
            match_day
        )
        SELECT *
        FROM (
            SELECT * FROM group_match_seed
            UNION ALL
            SELECT * FROM knockout_match_seed
        ) seeded_matches
        ORDER BY edition_id, kickoff_at, home_team_id, away_team_id
        RETURNING id
    )
    INSERT INTO synthetic_seed_registry (batch_id, table_name, primary_key)
    SELECT v_batch_id, 'match_game', jsonb_build_object('id', inserted.id)
    FROM inserted;

    WITH referee_seed AS (
        SELECT *
        FROM jsonb_to_recordset(v_referee_catalog) AS referee_seed(
            referee_seq INTEGER,
            full_name TEXT,
            country_name TEXT
        )
    ),
    referee_lookup AS (
        SELECT
            referee_seed.referee_seq,
            referee_row.id AS referee_id
        FROM referee_seed
        JOIN referee referee_row ON referee_row.full_name = referee_seed.full_name
    ),
    match_seed AS (
        SELECT
            match_row.id AS match_id,
            ROW_NUMBER() OVER (ORDER BY match_row.kickoff_at, match_row.id) AS match_seq
        FROM match_game match_row
        JOIN synthetic_seed_registry registry_row
            ON registry_row.batch_id = v_batch_id
           AND registry_row.table_name = 'match_game'
           AND registry_row.primary_key = jsonb_build_object('id', match_row.id)
    ),
    inserted AS (
        INSERT INTO match_official (match_id, referee_id, role)
        SELECT
            match_seed.match_id,
            referee_lookup.referee_id,
            assigned.role
        FROM match_seed
        CROSS JOIN LATERAL (
            VALUES
                (((match_seed.match_seq - 1) % 12) + 1, 'MAIN_REFEREE'::official_role),
                (((match_seed.match_seq + 2) % 12) + 1, 'ASSISTANT_REFEREE_1'::official_role),
                (((match_seed.match_seq + 5) % 12) + 1, 'ASSISTANT_REFEREE_2'::official_role),
                (((match_seed.match_seq + 8) % 12) + 1, 'FOURTH_OFFICIAL'::official_role)
        ) AS assigned(referee_seq, role)
        JOIN referee_lookup ON referee_lookup.referee_seq = assigned.referee_seq
        RETURNING match_id, referee_id
    )
    INSERT INTO synthetic_seed_registry (batch_id, table_name, primary_key)
    SELECT
        v_batch_id,
        'match_official',
        jsonb_build_object(
            'match_id', inserted.match_id,
            'referee_id', inserted.referee_id
        )
    FROM inserted;

    WITH seeded_matches AS (
        SELECT
            match_row.id AS match_id,
            match_row.edition_id,
            match_row.kickoff_at,
            match_row.match_day,
            match_row.home_team_id,
            match_row.away_team_id,
            match_row.winner_team_id,
            match_row.home_score,
            match_row.away_score,
            match_row.home_extra_score,
            match_row.away_extra_score,
            match_row.home_penalty_score,
            match_row.away_penalty_score,
            phase_row.code AS phase_code,
            team_group_row.group_letter
        FROM match_game match_row
        JOIN synthetic_seed_registry registry_row
            ON registry_row.batch_id = v_batch_id
           AND registry_row.table_name = 'match_game'
           AND registry_row.primary_key = jsonb_build_object('id', match_row.id)
        JOIN edition_phase edition_phase_row ON edition_phase_row.id = match_row.edition_phase_id
        JOIN competition_phase phase_row ON phase_row.id = edition_phase_row.phase_id
        LEFT JOIN team_group team_group_row ON team_group_row.id = match_row.group_id
    ),
    override_seed AS (
        SELECT
            seeded_matches.*,
            CASE
                WHEN seeded_matches.phase_code = 'GROUP_STAGE'
                 AND seeded_matches.group_letter IN ('A', 'C')
                 AND seeded_matches.match_day = 1
                 AND seeded_matches.kickoff_at::TIME = TIME '16:00'
                 AND seeded_matches.home_score >= 1
                    THEN 1
                ELSE 0
            END AS home_penalty_regular_count,
            CASE
                WHEN seeded_matches.phase_code = 'GROUP_STAGE'
                 AND seeded_matches.group_letter IN ('B', 'D')
                 AND seeded_matches.match_day = 2
                 AND seeded_matches.kickoff_at::TIME = TIME '20:00'
                 AND seeded_matches.away_score >= 1
                    THEN 1
                ELSE 0
            END AS away_penalty_regular_count,
            CASE
                WHEN seeded_matches.phase_code = 'GROUP_STAGE'
                 AND seeded_matches.group_letter = 'A'
                 AND seeded_matches.match_day = 2
                 AND seeded_matches.kickoff_at::TIME = TIME '16:00'
                 AND seeded_matches.home_score >= 1
                    THEN 1
                ELSE 0
            END AS home_goals_from_away_own_goal,
            CASE
                WHEN seeded_matches.phase_code = 'GROUP_STAGE'
                 AND seeded_matches.group_letter = 'D'
                 AND seeded_matches.match_day = 1
                 AND seeded_matches.kickoff_at::TIME = TIME '20:00'
                 AND seeded_matches.away_score >= 1
                    THEN 1
                ELSE 0
            END AS away_goals_from_home_own_goal
        FROM seeded_matches
    ),
    goal_plan AS (
        SELECT
            override_seed.match_id,
            override_seed.edition_id,
            override_seed.home_team_id AS team_id,
            'GOAL'::game_event_type AS event_type,
            CASE goal_index
                WHEN 1 THEN 9
                WHEN 2 THEN 10
                WHEN 3 THEN 11
                ELSE 8
            END::SMALLINT AS shirt_number,
            (12 + (goal_index - 1) * 17)::SMALLINT AS event_minute,
            0::SMALLINT AS stoppage_minute,
            'Open-play finish for the home side.'::TEXT AS description
        FROM override_seed
        CROSS JOIN LATERAL generate_series(
            1,
            GREATEST(
                override_seed.home_score
                - override_seed.home_penalty_regular_count
                - override_seed.home_goals_from_away_own_goal,
                0
            )
        ) AS goal_index(goal_index)

        UNION ALL

        SELECT
            override_seed.match_id,
            override_seed.edition_id,
            override_seed.home_team_id,
            'PENALTY_GOAL'::game_event_type,
            10::SMALLINT,
            44::SMALLINT,
            0::SMALLINT,
            'Penalty converted by the home side.'::TEXT
        FROM override_seed
        WHERE override_seed.home_penalty_regular_count = 1

        UNION ALL

        SELECT
            override_seed.match_id,
            override_seed.edition_id,
            override_seed.away_team_id,
            'OWN_GOAL'::game_event_type,
            4::SMALLINT,
            35::SMALLINT,
            0::SMALLINT,
            'Own goal that lifted the home team.'::TEXT
        FROM override_seed
        WHERE override_seed.home_goals_from_away_own_goal = 1

        UNION ALL

        SELECT
            override_seed.match_id,
            override_seed.edition_id,
            override_seed.away_team_id AS team_id,
            'GOAL'::game_event_type AS event_type,
            CASE goal_index
                WHEN 1 THEN 9
                WHEN 2 THEN 11
                WHEN 3 THEN 10
                ELSE 8
            END::SMALLINT AS shirt_number,
            (18 + (goal_index - 1) * 19)::SMALLINT AS event_minute,
            0::SMALLINT AS stoppage_minute,
            'Open-play finish for the away side.'::TEXT AS description
        FROM override_seed
        CROSS JOIN LATERAL generate_series(
            1,
            GREATEST(
                override_seed.away_score
                - override_seed.away_penalty_regular_count
                - override_seed.away_goals_from_home_own_goal,
                0
            )
        ) AS goal_index(goal_index)

        UNION ALL

        SELECT
            override_seed.match_id,
            override_seed.edition_id,
            override_seed.away_team_id,
            'PENALTY_GOAL'::game_event_type,
            10::SMALLINT,
            52::SMALLINT,
            0::SMALLINT,
            'Penalty converted by the away side.'::TEXT
        FROM override_seed
        WHERE override_seed.away_penalty_regular_count = 1

        UNION ALL

        SELECT
            override_seed.match_id,
            override_seed.edition_id,
            override_seed.home_team_id,
            'OWN_GOAL'::game_event_type,
            5::SMALLINT,
            41::SMALLINT,
            0::SMALLINT,
            'Own goal that brought the away team back into the match.'::TEXT
        FROM override_seed
        WHERE override_seed.away_goals_from_home_own_goal = 1

        UNION ALL

        SELECT
            override_seed.match_id,
            override_seed.edition_id,
            override_seed.home_team_id,
            'GOAL'::game_event_type,
            CASE goal_index
                WHEN 1 THEN 9
                ELSE 10
            END::SMALLINT,
            (96 + (goal_index - 1) * 12)::SMALLINT,
            0::SMALLINT,
            'Extra-time goal for the home side.'::TEXT
        FROM override_seed
        CROSS JOIN LATERAL generate_series(1, GREATEST(override_seed.home_extra_score, 0)) AS goal_index(goal_index)

        UNION ALL

        SELECT
            override_seed.match_id,
            override_seed.edition_id,
            override_seed.away_team_id,
            'GOAL'::game_event_type,
            CASE goal_index
                WHEN 1 THEN 9
                ELSE 10
            END::SMALLINT,
            (103 + (goal_index - 1) * 10)::SMALLINT,
            0::SMALLINT,
            'Extra-time goal for the away side.'::TEXT
        FROM override_seed
        CROSS JOIN LATERAL generate_series(1, GREATEST(override_seed.away_extra_score, 0)) AS goal_index(goal_index)
    ),
    card_plan AS (
        SELECT
            seeded_matches.match_id,
            seeded_matches.edition_id,
            CASE
                WHEN mod(seeded_matches.match_id, 2) = 0 THEN seeded_matches.away_team_id
                ELSE seeded_matches.home_team_id
            END AS team_id,
            'YELLOW_CARD'::game_event_type AS event_type,
            6::SMALLINT AS shirt_number,
            63::SMALLINT AS event_minute,
            0::SMALLINT AS stoppage_minute,
            'Late tactical yellow card.'::TEXT AS description
        FROM seeded_matches

        UNION ALL

        SELECT
            seeded_matches.match_id,
            seeded_matches.edition_id,
            CASE
                WHEN seeded_matches.home_team_id = seeded_matches.winner_team_id THEN seeded_matches.home_team_id
                WHEN seeded_matches.away_team_id = seeded_matches.winner_team_id THEN seeded_matches.away_team_id
                ELSE seeded_matches.home_team_id
            END AS team_id,
            'SUBSTITUTION'::game_event_type AS event_type,
            9::SMALLINT AS shirt_number,
            72::SMALLINT AS event_minute,
            0::SMALLINT AS stoppage_minute,
            'Attacking substitution to manage the final phase of the match.'::TEXT AS description
        FROM seeded_matches

        UNION ALL

        SELECT
            seeded_matches.match_id,
            seeded_matches.edition_id,
            CASE
                WHEN seeded_matches.winner_team_id = seeded_matches.home_team_id THEN seeded_matches.away_team_id
                WHEN seeded_matches.winner_team_id = seeded_matches.away_team_id THEN seeded_matches.home_team_id
                ELSE seeded_matches.away_team_id
            END AS team_id,
            'RED_CARD'::game_event_type,
            4::SMALLINT,
            85::SMALLINT,
            0::SMALLINT,
            'Straight red card after denying a clear chance.'::TEXT
        FROM seeded_matches
        WHERE seeded_matches.phase_code IN ('SEMIFINAL', 'FINAL')
    ),
    inserted AS (
        INSERT INTO match_event (
            match_id,
            event_minute,
            stoppage_minute,
            event_type,
            team_id,
            player_id,
            related_player_id,
            description
        )
        SELECT
            goal_plan.match_id,
            goal_plan.event_minute,
            goal_plan.stoppage_minute,
            goal_plan.event_type,
            goal_plan.team_id,
            call_up_row.player_id,
            NULL::BIGINT,
            goal_plan.description
        FROM goal_plan
        JOIN team_call_up call_up_row
            ON call_up_row.edition_id = goal_plan.edition_id
           AND call_up_row.team_id = goal_plan.team_id
           AND call_up_row.shirt_number = goal_plan.shirt_number

        UNION ALL

        SELECT
            card_plan.match_id,
            card_plan.event_minute,
            card_plan.stoppage_minute,
            card_plan.event_type,
            card_plan.team_id,
            outgoing_row.player_id,
            CASE
                WHEN card_plan.event_type = 'SUBSTITUTION' THEN incoming_row.player_id
                ELSE NULL::BIGINT
            END AS related_player_id,
            card_plan.description
        FROM card_plan
        JOIN team_call_up outgoing_row
            ON outgoing_row.edition_id = card_plan.edition_id
           AND outgoing_row.team_id = card_plan.team_id
           AND outgoing_row.shirt_number = card_plan.shirt_number
        LEFT JOIN team_call_up incoming_row
            ON incoming_row.edition_id = card_plan.edition_id
           AND incoming_row.team_id = card_plan.team_id
           AND incoming_row.shirt_number = 14
        RETURNING id
    )
    INSERT INTO synthetic_seed_registry (batch_id, table_name, primary_key)
    SELECT v_batch_id, 'match_event', jsonb_build_object('id', inserted.id)
    FROM inserted;

    UPDATE world_cup_edition edition_row
    SET champion_team_id = champion_team.id,
        vice_champion_team_id = vice_team.id,
        third_place_team_id = third_team.id
    FROM team champion_team,
         team vice_team,
         team third_team
    WHERE edition_row.year = 2034
      AND champion_team.fifa_code = 'AUR'
      AND vice_team.fifa_code = 'IVE'
      AND third_team.fifa_code = 'MRD';

    UPDATE world_cup_edition edition_row
    SET champion_team_id = champion_team.id,
        vice_champion_team_id = vice_team.id,
        third_place_team_id = third_team.id
    FROM team champion_team,
         team vice_team,
         team third_team
    WHERE edition_row.year = 2038
      AND champion_team.fifa_code = 'IVE'
      AND vice_team.fifa_code = 'AUR'
      AND third_team.fifa_code = 'FJD';

    RETURN QUERY
    SELECT
        'populate'::TEXT AS operation,
        'seeded'::TEXT AS status,
        summary_row.batch_id AS active_batch_id,
        summary_row.edition_years,
        summary_row.table_counts,
        summary_row.total_rows,
        'Synthetic dataset seeded successfully.'::TEXT AS message
    FROM fn_internal_synthetic_batch_summary(v_batch_id) summary_row;
END;
$synthetic$;

CREATE OR REPLACE FUNCTION fn_cleanup_synthetic_data()
RETURNS TABLE (
    operation TEXT,
    status TEXT,
    active_batch_id BIGINT,
    edition_years SMALLINT[],
    table_counts JSONB,
    total_rows BIGINT,
    message TEXT
)
LANGUAGE plpgsql
SET search_path = world_cup, public
AS $cleanup$
DECLARE
    v_batch_id BIGINT;
    v_table_name TEXT;
    v_registry_row RECORD;
    v_expected_count BIGINT;
    v_deleted_count BIGINT;
    v_deleted_step BIGINT;
BEGIN
    PERFORM fn_internal_acquire_synthetic_seed_lock();

    SELECT batch_row.id
    INTO v_batch_id
    FROM synthetic_seed_batch batch_row
    WHERE batch_row.is_active = TRUE
    ORDER BY batch_row.id DESC
    LIMIT 1;

    IF v_batch_id IS NULL THEN
        RETURN QUERY
        SELECT
            'cleanup'::TEXT AS operation,
            'nothing_to_clean'::TEXT AS status,
            NULL::BIGINT AS active_batch_id,
            ARRAY[]::SMALLINT[] AS edition_years,
            '{}'::JSONB AS table_counts,
            0::BIGINT AS total_rows,
            'No active synthetic dataset was found.'::TEXT AS message;
        RETURN;
    END IF;

    FOR v_table_name IN
        SELECT unnest(ARRAY[
            'match_event',
            'match_official',
            'match_game',
            'team_call_up',
            'edition_team',
            'team_group',
            'edition_phase',
            'edition_host_city',
            'world_cup_edition',
            'stadium',
            'host_city',
            'player',
            'referee',
            'coach',
            'competition_phase',
            'team',
            'country',
            'confederation'
        ]::TEXT[])
    LOOP
        SELECT COUNT(*)::BIGINT
        INTO v_expected_count
        FROM synthetic_seed_registry registry_row
        WHERE registry_row.batch_id = v_batch_id
          AND registry_row.table_name = v_table_name;

        v_deleted_count := 0;

        FOR v_registry_row IN
            SELECT registry_row.primary_key
            FROM synthetic_seed_registry registry_row
            WHERE registry_row.batch_id = v_batch_id
              AND registry_row.table_name = v_table_name
            ORDER BY registry_row.id DESC
        LOOP
            CASE v_table_name
                WHEN 'match_event' THEN
                    DELETE FROM match_event
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'match_official' THEN
                    DELETE FROM match_official
                    WHERE match_id = (v_registry_row.primary_key ->> 'match_id')::BIGINT
                      AND referee_id = (v_registry_row.primary_key ->> 'referee_id')::BIGINT;
                WHEN 'match_game' THEN
                    DELETE FROM match_game
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'team_call_up' THEN
                    DELETE FROM team_call_up
                    WHERE edition_id = (v_registry_row.primary_key ->> 'edition_id')::BIGINT
                      AND team_id = (v_registry_row.primary_key ->> 'team_id')::BIGINT
                      AND player_id = (v_registry_row.primary_key ->> 'player_id')::BIGINT;
                WHEN 'edition_team' THEN
                    DELETE FROM edition_team
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'team_group' THEN
                    DELETE FROM team_group
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT
                      AND edition_id = (v_registry_row.primary_key ->> 'edition_id')::BIGINT;
                WHEN 'edition_phase' THEN
                    DELETE FROM edition_phase
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT
                      AND edition_id = (v_registry_row.primary_key ->> 'edition_id')::BIGINT;
                WHEN 'edition_host_city' THEN
                    DELETE FROM edition_host_city
                    WHERE edition_id = (v_registry_row.primary_key ->> 'edition_id')::BIGINT
                      AND host_city_id = (v_registry_row.primary_key ->> 'host_city_id')::BIGINT;
                WHEN 'world_cup_edition' THEN
                    DELETE FROM world_cup_edition
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'stadium' THEN
                    DELETE FROM stadium
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'host_city' THEN
                    DELETE FROM host_city
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'player' THEN
                    DELETE FROM player
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'referee' THEN
                    DELETE FROM referee
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'coach' THEN
                    DELETE FROM coach
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'competition_phase' THEN
                    DELETE FROM competition_phase
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'team' THEN
                    DELETE FROM team
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'country' THEN
                    DELETE FROM country
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                WHEN 'confederation' THEN
                    DELETE FROM confederation
                    WHERE id = (v_registry_row.primary_key ->> 'id')::BIGINT;
                ELSE
                    RAISE EXCEPTION 'Unsupported synthetic cleanup table: %', v_table_name;
            END CASE;

            GET DIAGNOSTICS v_deleted_step = ROW_COUNT;
            v_deleted_count := v_deleted_count + v_deleted_step;
        END LOOP;

        IF v_deleted_count <> v_expected_count THEN
            RAISE EXCEPTION
                'Synthetic cleanup drift detected for table %: expected %, deleted %.',
                v_table_name,
                v_expected_count,
                v_deleted_count;
        END IF;
    END LOOP;

    UPDATE synthetic_seed_batch
    SET is_active = FALSE,
        cleaned_at = NOW()
    WHERE id = v_batch_id;

    RETURN QUERY
    SELECT
        'cleanup'::TEXT AS operation,
        'cleaned'::TEXT AS status,
        summary_row.batch_id AS active_batch_id,
        summary_row.edition_years,
        summary_row.table_counts,
        summary_row.total_rows,
        'Synthetic dataset cleaned successfully.'::TEXT AS message
    FROM fn_internal_synthetic_batch_summary(v_batch_id) summary_row;
END;
$cleanup$;

COMMIT;
