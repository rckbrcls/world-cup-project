BEGIN;

SET search_path TO world_cup, public;

CREATE TABLE synthetic_seed_batch (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    dataset_key VARCHAR(80) NOT NULL,
    edition_years SMALLINT[] NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    cleaned_at TIMESTAMPTZ,
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT synthetic_seed_batch_timeline CHECK (
        cleaned_at IS NULL OR cleaned_at >= created_at
    )
);

CREATE TABLE synthetic_seed_registry (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    batch_id BIGINT NOT NULL REFERENCES synthetic_seed_batch (id) ON UPDATE CASCADE ON DELETE CASCADE,
    table_name VARCHAR(63) NOT NULL,
    primary_key JSONB NOT NULL,
    created_at TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    CONSTRAINT synthetic_seed_registry_pk_object CHECK (jsonb_typeof(primary_key) = 'object'),
    UNIQUE (batch_id, table_name, primary_key)
);

CREATE UNIQUE INDEX idx_synthetic_seed_batch_single_active
    ON synthetic_seed_batch (is_active)
    WHERE is_active = TRUE;

CREATE INDEX idx_synthetic_seed_registry_batch_table
    ON synthetic_seed_registry (batch_id, table_name);

CREATE OR REPLACE FUNCTION fn_internal_acquire_synthetic_seed_lock()
RETURNS VOID
LANGUAGE plpgsql
SET search_path = world_cup, public
AS $$
BEGIN
    PERFORM pg_advisory_xact_lock(hashtextextended('world_cup.synthetic_seed', 0));
END;
$$;

CREATE OR REPLACE FUNCTION fn_internal_register_synthetic_row(
    p_batch_id BIGINT,
    p_table_name TEXT,
    p_primary_key JSONB
)
RETURNS VOID
LANGUAGE plpgsql
SET search_path = world_cup, public
AS $$
BEGIN
    INSERT INTO synthetic_seed_registry (batch_id, table_name, primary_key)
    VALUES (p_batch_id, p_table_name, p_primary_key);
END;
$$;

CREATE OR REPLACE FUNCTION fn_internal_synthetic_batch_summary(p_batch_id BIGINT)
RETURNS TABLE (
    batch_id BIGINT,
    dataset_key VARCHAR(80),
    edition_years SMALLINT[],
    created_at TIMESTAMPTZ,
    cleaned_at TIMESTAMPTZ,
    table_counts JSONB,
    total_rows BIGINT
)
LANGUAGE sql
SET search_path = world_cup, public
AS $$
    WITH registry_counts AS (
        SELECT
            registry_row.batch_id,
            registry_row.table_name,
            COUNT(*)::BIGINT AS row_count
        FROM synthetic_seed_registry registry_row
        WHERE registry_row.batch_id = p_batch_id
        GROUP BY registry_row.batch_id, registry_row.table_name
    )
    SELECT
        batch_row.id AS batch_id,
        batch_row.dataset_key,
        batch_row.edition_years,
        batch_row.created_at,
        batch_row.cleaned_at,
        COALESCE(
            jsonb_object_agg(registry_counts.table_name, registry_counts.row_count ORDER BY registry_counts.table_name)
                FILTER (WHERE registry_counts.table_name IS NOT NULL),
            '{}'::JSONB
        ) AS table_counts,
        COALESCE(SUM(registry_counts.row_count), 0)::BIGINT AS total_rows
    FROM synthetic_seed_batch batch_row
    LEFT JOIN registry_counts ON registry_counts.batch_id = batch_row.id
    WHERE batch_row.id = p_batch_id
    GROUP BY
        batch_row.id,
        batch_row.dataset_key,
        batch_row.edition_years,
        batch_row.created_at,
        batch_row.cleaned_at;
$$;

CREATE OR REPLACE FUNCTION fn_synthetic_data_status()
RETURNS TABLE (
    has_active_batch BOOLEAN,
    active_batch_id BIGINT,
    dataset_key VARCHAR(80),
    edition_years SMALLINT[],
    created_at TIMESTAMPTZ,
    cleaned_at TIMESTAMPTZ,
    table_counts JSONB,
    total_rows BIGINT,
    history_batch_count BIGINT
)
LANGUAGE sql
SET search_path = world_cup, public
AS $$
    WITH active_batch AS (
        SELECT batch_row.id AS batch_id
        FROM synthetic_seed_batch batch_row
        WHERE batch_row.is_active = TRUE
        ORDER BY batch_row.id DESC
        LIMIT 1
    ),
    latest_batch AS (
        SELECT batch_row.id AS batch_id
        FROM synthetic_seed_batch batch_row
        ORDER BY batch_row.created_at DESC, batch_row.id DESC
        LIMIT 1
    ),
    selected_batch AS (
        SELECT COALESCE(
            (SELECT batch_id FROM active_batch),
            (SELECT batch_id FROM latest_batch)
        ) AS batch_id
    ),
    selected_summary AS (
        SELECT *
        FROM fn_internal_synthetic_batch_summary((SELECT batch_id FROM selected_batch))
    )
    SELECT
        COALESCE((SELECT TRUE FROM active_batch LIMIT 1), FALSE) AS has_active_batch,
        (SELECT batch_id FROM active_batch LIMIT 1) AS active_batch_id,
        selected_summary.dataset_key,
        COALESCE(selected_summary.edition_years, ARRAY[]::SMALLINT[]) AS edition_years,
        selected_summary.created_at,
        selected_summary.cleaned_at,
        COALESCE(selected_summary.table_counts, '{}'::JSONB) AS table_counts,
        COALESCE(selected_summary.total_rows, 0)::BIGINT AS total_rows,
        (SELECT COUNT(*)::BIGINT FROM synthetic_seed_batch) AS history_batch_count
    FROM (SELECT 1) singleton
    LEFT JOIN selected_summary ON TRUE;
$$;

